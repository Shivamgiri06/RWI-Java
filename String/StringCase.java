//String toLowerCase() and String toUpperCase()

public class StringCase {
    public static void main(String[] args) {
        String str = "Java";
        
        //String toLowerCase()
        str = str.toLowerCase();
        System.out.println(str);

        //String toUpperCase()
        String str1 = str.toUpperCase();
        System.out.println(str1);

    }
}
